class PrimitiveDataType {
    static int a;
    static char ch;
    static long c;
    static float d;
    static double e;
    static short f;
    static boolean g;
    static byte h;


    void display(){
        System.out.println("Default value of Primitive DataType is : ");
        System.out.println("Default value of int = " + a);
        System.out.println("Default value of char = " + ch);
        System.out.println("Default value of long = " + c);
        System.out.println("Default value of float = " + d);
        System.out.println("Default value of double = " + e);
        System.out.println("Default value of float = " + f);
        System.out.println("Default value of boolean = " + g);
        System.out.println("Default value of byte = " + h);

    }
}
